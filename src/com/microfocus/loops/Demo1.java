package com.microfocus.loops;

public class Demo1 {

    public static void main(String[] args) {

        //1 to 10
        for(int i=1;i<=10;i++)
        {
            System.out.println(i);
        }

        String[] colors1={"red","green","yellow","pink"};

        for(int i=0;i<colors1.length;i++)
        {
            System.out.println(colors1[i]);
        }

    }
}
